"""
Доменные исключения — чистый Python, без зависимостей на инфраструктуру.
Любое нарушение бизнес-правил выбрасывает подкласс DomainError.
"""


class DomainError(Exception):
    """Базовый класс для всех нарушений доменных правил."""


# ── Исключения лида ────────────────────────────────────────────────────────────

class LeadNotQualifiedError(DomainError):
    """Попытка конвертации лида, который ещё не квалифицирован."""


class LeadAlreadyConvertedError(DomainError):
    """Попытка изменить статус уже конвертированного лида."""


class InvalidLeadTransitionError(DomainError):
    """Переход между статусами лида не разрешён машиной состояний."""


# ── Исключения сделки ──────────────────────────────────────────────────────────

class DealAlreadyClosedError(DomainError):
    """Попытка изменить закрытую (выигранную или проигранную) сделку."""


class InvalidStageForPipelineError(DomainError):
    """Этап принадлежит другой воронке продаж."""


# ── Исключения воронки / этапа ─────────────────────────────────────────────────

class PipelineStageOrderError(DomainError):
    """Конфликт порядкового номера этапа внутри воронки."""


class StageNotFoundError(DomainError):
    """Этап не найден в воронке."""


# ── Исключения объектов-значений ───────────────────────────────────────────────

class InvalidEmailError(DomainError):
    """Адрес электронной почты не прошёл валидацию формата."""


class InvalidMoneyAmountError(DomainError):
    """Денежная сумма или код валюты недопустимы."""


class InvalidPhoneError(DomainError):
    """Номер телефона не прошёл валидацию формата."""
